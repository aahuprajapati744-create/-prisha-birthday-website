const confCanvas=$('confettiCanvas');
const confCtx=confCanvas&&confCanvas.getContext('2d');
let pieces=[], rafId=null;
const COLORS=['#e91e8c','#f48fb1','#ffc107','#ffd54f','#f06292','#ff80ab','#fff0f6','#ffca28','#ff4081'];
function resizeConf(){if(confCanvas){confCanvas.width=innerWidth;confCanvas.height=innerHeight;}}
window.addEventListener('resize',resizeConf); resizeConf();

function launchConfetti(count=100){
  for(let i=0;i<count;i++) pieces.push({x:Math.random()*confCanvas.width,y:-10-Math.random()*confCanvas.height*0.3,w:5+Math.random()*9,h:8+Math.random()*14,color:COLORS[Math.floor(Math.random()*COLORS.length)],vx:-2+Math.random()*4,vy:2+Math.random()*4,rot:Math.random()*360,rotV:-4+Math.random()*8,op:1});
  if(!rafId) animConf();
}
function animConf(){
  confCtx.clearRect(0,0,confCanvas.width,confCanvas.height);
  pieces=pieces.filter(p=>p.op>0.04&&p.y<confCanvas.height+30);
  pieces.forEach(p=>{
    p.x+=p.vx;p.y+=p.vy;p.rot+=p.rotV;p.vy+=0.06;
    if(p.y>confCanvas.height*0.7) p.op-=0.012;
    confCtx.save();confCtx.globalAlpha=Math.max(0,p.op);
    confCtx.translate(p.x+p.w/2,p.y+p.h/2);confCtx.rotate(p.rot*Math.PI/180);
    confCtx.fillStyle=p.color;confCtx.fillRect(-p.w/2,-p.h/2,p.w,p.h);confCtx.restore();
  });
  if(pieces.length){rafId=requestAnimationFrame(animConf);}else{confCtx.clearRect(0,0,confCanvas.width,confCanvas.height);rafId=null;}
}

function launchFireworks(count=5){
  for(let i=0;i<count;i++){
    setTimeout(()=>{
      const cx=confCanvas.width*(0.2+Math.random()*0.6),cy=confCanvas.height*(0.1+Math.random()*0.35);
      for(let j=0;j<32;j++){
        const ang=(j/32)*Math.PI*2,spd=4+Math.random()*7;
        pieces.push({x:cx,y:cy,w:3,h:3,color:COLORS[Math.floor(Math.random()*COLORS.length)],vx:Math.cos(ang)*spd,vy:Math.sin(ang)*spd,rot:0,rotV:0,op:1});
      }
      if(!rafId) animConf();
    },i*280);
  }
}

