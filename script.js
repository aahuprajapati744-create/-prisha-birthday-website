/* ============================================================
   HERO PARTICLES CANVAS
   ============================================================ */
function setupHeroParticles() {
  const c = $('heroParticles');
  if (!c) return;
  c.width = innerWidth; c.height = innerHeight;
  const ctx = c.getContext('2d');
  const EMOJIS = ['✨','⭐','💫','🌟','💕','💖','🌸'];
  const pts = [];
  for (let i = 0; i < 30; i++) pts.push({ x:Math.random()*c.width, y:Math.random()*c.height, vx:(-0.3+Math.random()*0.6), vy:-0.2-Math.random()*0.5, emoji:EMOJIS[Math.floor(Math.random()*EMOJIS.length)], size:10+Math.random()*12, op:Math.random(), phase:Math.random()*Math.PI*2 });
  let t = 0;
  window.addEventListener('resize', () => { c.width=innerWidth; c.height=innerHeight; });
  function draw() {
    ctx.clearRect(0,0,c.width,c.height);
    t += 0.016;
    pts.forEach(p => {
      p.x += p.vx + Math.sin(t*0.5+p.phase)*0.2;
      p.y += p.vy;
      if (p.y < -30) { p.y = c.height+20; p.x = Math.random()*c.width; }
      const op = 0.3 + 0.4*Math.abs(Math.sin(t*0.8+p.phase));
      ctx.globalAlpha = op;
      ctx.font = `${p.size}px serif`;
      ctx.fillText(p.emoji, p.x, p.y);
    });
    ctx.globalAlpha = 1;
    requestAnimationFrame(draw);
  }
  draw();
}

/* ============================================================
   AMBIENT BACKGROUND CANVAS
   ============================================================ */
function setupAmbientCanvas() {
  const c = $('ambientCanvas');
  if (!c) return;
  c.width = innerWidth; c.height = innerHeight;
  const ctx = c.getContext('2d');
  const pts = [];
  for (let i = 0; i < 50; i++) {
    pts.push({ x: Math.random()*c.width, y: Math.random()*c.height, r: 1+Math.random()*3, vx:(-0.2+Math.random()*0.4), vy:(-0.2+Math.random()*0.4), op:Math.random(), phase:Math.random()*Math.PI*2, color: Math.random() > 0.5 ? '#e91e8c' : '#ffc107' });
  }
  let t = 0;
  window.addEventListener('resize', () => { c.width=innerWidth; c.height=innerHeight; });
  function draw() {
    ctx.clearRect(0,0,c.width,c.height);
    t += 0.008;
    pts.forEach(p => {
      p.x += p.vx; p.y += p.vy;
      if (p.x<0||p.x>c.width) p.vx*=-1;
      if (p.y<0||p.y>c.height) p.vy*=-1;
      const op = 0.15 + 0.25*Math.abs(Math.sin(t+p.phase));
      ctx.beginPath(); ctx.arc(p.x,p.y,p.r,0,Math.PI*2);
      ctx.fillStyle = p.color; ctx.globalAlpha = op; ctx.fill();
    });
    ctx.globalAlpha = 1;
    requestAnimationFrame(draw);
  }
  draw();
}

