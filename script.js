/* ============================================================
   SECTION 1 — LOADING SCREEN (upgraded)
   ============================================================ */
function startLoader() {
  const messages = ["Collecting memories…","Wrapping your gifts…","Finding our favourite moments…","Almost ready… 💖"];
  const quotes = CONFIG.romanticQuotes;
  const fill = $('loaderFill'), msgEl = $('loaderMessage'), pctEl = $('loaderPct');
  const quoteEl = $('loaderQuote');
  const total = 2600;
  let progress = 0, qi = 0, mi = 0;

  // Loader canvas particles
  const lc = $('loaderCanvas');
  if (lc) {
    lc.width = innerWidth; lc.height = innerHeight;
    const lctx = lc.getContext('2d');
    const lps = [];
    for (let i = 0; i < 60; i++) lps.push({ x: Math.random()*lc.width, y: Math.random()*lc.height, vx:(-0.5+Math.random()), vy:(-0.5+Math.random()), r: 2+Math.random()*4, op: Math.random() });
    function animLp() {
      if (!$('loader').classList.contains('out')) {
        lctx.clearRect(0,0,lc.width,lc.height);
        lps.forEach(p => {
          p.x+=p.vx; p.y+=p.vy; p.op+=0.01*(Math.random()>0.5?1:-1);
          if(p.x<0||p.x>lc.width) p.vx*=-1; if(p.y<0||p.y>lc.height) p.vy*=-1;
          p.op = Math.max(0.1, Math.min(0.8, p.op));
          lctx.beginPath(); lctx.arc(p.x,p.y,p.r,0,Math.PI*2);
          lctx.fillStyle = `rgba(233,30,140,${p.op})`; lctx.fill();
        });
        requestAnimationFrame(animLp);
      }
    }
    animLp();
  }

  // Cycle messages
  const mTimer = setInterval(() => {
    mi = Math.min(mi+1, messages.length-1);
    msgEl.style.opacity='0';
    setTimeout(() => { msgEl.textContent = messages[mi]; msgEl.style.opacity='1'; }, 250);
  }, total/messages.length);

  // Cycle quotes
  const qTimer = setInterval(() => {
    qi = (qi+1) % quotes.length;
    quoteEl.style.opacity='0';
    setTimeout(() => { quoteEl.textContent = quotes[qi]; quoteEl.style.opacity='1'; }, 250);
  }, 1800);

  // Progress bar
  const fillInt = setInterval(() => {
    progress = Math.min(100, progress + (100/(total/16)));
    fill.style.width = progress + '%';
    if (pctEl) pctEl.textContent = Math.floor(progress) + '%';
    if (progress >= 100) {
      clearInterval(fillInt); clearInterval(mTimer); clearInterval(qTimer);
      setTimeout(() => {
        $('loader').classList.add('out');
        $('siteControls').classList.add('show');
        animateHeroTitle();
      }, 350);
    }
  }, 16);
}

