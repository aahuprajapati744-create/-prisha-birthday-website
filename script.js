function secretCanvasBurst() {
  const c = $('secretCanvas');
  if (!c) return;
  c.width = c.parentElement.offsetWidth; c.height = c.parentElement.offsetHeight;
  const ctx = c.getContext('2d');
  const pts = [];
  const cx = c.width/2, cy = c.height/2;
  for (let i=0;i<120;i++) {
    const ang = Math.random()*Math.PI*2, spd = 3+Math.random()*8;
    pts.push({ x:cx, y:cy, vx:Math.cos(ang)*spd, vy:Math.sin(ang)*spd, r:3+Math.random()*5, color:['#e91e8c','#ffc107','#f48fb1','#ff80ab','#ffd54f'][Math.floor(Math.random()*5)], op:1 });
  }
  function draw() {
    ctx.clearRect(0,0,c.width,c.height);
    let alive = false;
    pts.forEach(p => {
      p.x+=p.vx; p.y+=p.vy; p.vy+=0.08; p.op-=0.018;
      if (p.op>0) { alive=true; ctx.beginPath(); ctx.arc(p.x,p.y,p.r,0,Math.PI*2); ctx.fillStyle=p.color; ctx.globalAlpha=p.op; ctx.fill(); }
    });
    ctx.globalAlpha=1;
    if (alive) requestAnimationFrame(draw);
    else ctx.clearRect(0,0,c.width,c.height);
  }
  draw();
}

