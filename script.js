/* ============================================================
   COUNTDOWN with flip animation
   ============================================================ */
let prevH = -1, prevM = -1, prevS = -1;
function setupCountdown() {
  function tick() {
    const now = new Date();
    const end = new Date(now.getFullYear(), now.getMonth(), now.getDate(), 23, 59, 59);
    const diff = Math.max(0, end - now);
    const h = Math.floor(diff/3600000), m = Math.floor((diff%3600000)/60000), s = Math.floor((diff%60000)/1000);
    if (h !== prevH) { flip('cdH', h); prevH=h; }
    if (m !== prevM) { flip('cdM', m); prevM=m; }
    if (s !== prevS) { flip('cdS', s); prevS=s; }
  }
  function flip(id, val) {
    const el = $(id); if(!el) return;
    el.textContent = String(val).padStart(2,'0');
    const block = el.closest('.cd-block');
    if (block) { block.classList.remove('flip'); void block.offsetWidth; block.classList.add('flip'); }
  }
  tick(); setInterval(tick, 1000);
}

