/* ============================================================
   CUSTOM CURSOR + HEART TRAIL
   ============================================================ */
function initCursor() {
  const cur = $('cursor'), trail = $('cursorTrail');
  if (!cur) return;
  const HEARTS = ['💖','💕','✨','🌸','💗'];
  let lastHeart = 0;
  document.addEventListener('mousemove', e => {
    mouseX = e.clientX; mouseY = e.clientY;
    // Spawn heart trail
    if (Date.now() - lastHeart > 80) {
      lastHeart = Date.now();
      const h = document.createElement('div');
      h.className = 'heart-trail';
      h.textContent = HEARTS[Math.floor(Math.random()*HEARTS.length)];
      h.style.cssText = `left:${mouseX}px;top:${mouseY}px;`;
      document.body.appendChild(h);
      setTimeout(() => h.remove(), 800);
    }
    // Hover effect on interactive elements
    const el = document.elementFromPoint(mouseX, mouseY);
    const isClickable = el && (el.tagName==='BUTTON'||el.tagName==='A'||el.closest('button')||el.closest('a')||el.closest('.gallery-item')||el.closest('.envelope-card')||el.closest('.reason-flip')||el.closest('.travel-card'));
    cur.classList.toggle('big', !!isClickable);
  });
}

function cursorLoop() {
  const cur = $('cursor'), trail = $('cursorTrail');
  if (!cur) { requestAnimationFrame(cursorLoop); return; }
  cursorX += (mouseX - cursorX) * 0.25;
  cursorY += (mouseY - cursorY) * 0.25;
  trailX += (mouseX - trailX) * 0.12;
  trailY += (mouseY - trailY) * 0.12;
  cur.style.cssText = `left:${mouseX}px;top:${mouseY}px;`;
  if (trail) trail.style.cssText = `left:${trailX}px;top:${trailY}px;`;
  requestAnimationFrame(cursorLoop);
}

/* ============================================================
   SCROLL PROGRESS BAR
   ============================================================ */
function initScrollBar() {
  const bar = $('scrollBar');
  if (!bar) return;
  window.addEventListener('scroll', () => {
    const pct = (scrollY / (document.documentElement.scrollHeight - innerHeight)) * 100;
    bar.style.width = pct + '%';
  }, { passive: true });
}

/* ============================================================
   PARALLAX
   ============================================================ */
function initParallax() {
  window.addEventListener('scroll', () => {
    const hero = $('hero');
    if (!hero) return;
    const offset = scrollY * 0.25;
    hero.style.backgroundPositionY = `-${offset}px`;
    // Move orbs with parallax
    $$('.orb').forEach((el, i) => { el.style.transform = `translate(0, ${scrollY*(0.08*(i+1))}px)`; });
  }, { passive: true });
}

